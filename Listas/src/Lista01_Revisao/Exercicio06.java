package Lista01_Revisao;

public class Exercicio06 {
    public static void main(String[] args) {

        System.out.println("Imprimir números ímpares de 1 a 50.");

        for (int i = 1; i <= 50; i++) {
            if (i % 2 != 0) {
                System.out.println(i);
            }

        }

    }
}
