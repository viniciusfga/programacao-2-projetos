package Lista01_Revisao;

public class Exercicio04 {
    public static void main(String[] args) {

        System.out.println("Calcular o produto de 1 a 10.");

        int prod = 1;
        for (int i = 1; i <= 10; i++) {
            prod *= i;
        }
        System.out.println(prod);


    }
}
